   private void getmarks(String stid,String sub){
       
        double theory= 0;
        double practical=0;
        String grade="";
        
        String subid="";
       
        
            
        String query1="select * from subject  where sub_name=?";
        try {
                pdst=con.connect().prepareStatement(query1);
                pdst.setString(1, sub);
            
            ResultSet rs=pdst.executeQuery();
            while(rs.next()){
                subid=rs.getString("sub_id");
            
            }
            String query2="select * from final_marks where sub_id=? and stu_id=?";
        
            pdst=con.connect().prepareStatement(query2);
             pdst.setString(1, subid); 
             pdst.setString(2, stid);
            
            rs=pdst.executeQuery();
            
            //get result for result set
            while(rs.next()){
                practical=rs.getDouble("practical");
                theory=rs.getDouble("theory");
                
                    
            
            //get full ca marks total
            double Finaltotal=(theory+practical)/2;
             // check CA pass or fail
            String CAStatus;
            
            double catotal=vc.getmarks(get_text(stid),get_text2(sub));
            System.out.println(catotal);
            
            if(catotal>=50){
                CAStatus="PASS";
            }
            else{
                 CAStatus = "FAIL";
            }
            
            double marks=(catotal+Finaltotal)/2;
            
            //calculate LecGrade
            if(CAStatus=="FAIL"){
               
                    grade="F";
            }
            else if(marks>=85){
                grade="A+";
            }
            else if(marks>=75){
                grade="A";
            }
            else if(marks>=70){
                grade="B+";
            }
            else if(marks>=65){
                grade="B";
            }
            else if(marks>=60){
                grade="C+";
            }
            else if(marks>=55){
                grade="C";
            }
            else if(marks>=50){
                grade="C-";
            }
            else if(marks>=45){
                grade="D+";
            }
             else if(marks>=35){
                grade="D-";
            }
            else{
                 grade="F";
             }
            
            
            tmodel=(DefaultTableModel)table.getModel();
                    tmodel.addRow(new Object[]{
                        stid,
                        subid,
                        CAStatus,
                        grade
                        
                    });
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(St_ca.class.getName()).log(Level.SEVERE, null, ex);
        }
    
    }
