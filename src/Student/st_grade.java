    private void subject(){
        try {
            pdst=con.connect().prepareStatement("select * from subject");
             ResultSet rs=pdst.executeQuery();
             subname.removeAllItems();
            
             while(rs.next()){
                 subname.addItem(rs.getString(3));
             }
            
            
        } catch (SQLException ex) {
            Logger.getLogger(St_ca.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }
