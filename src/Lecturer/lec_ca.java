    public double getmarks(String stid,String sub){
        
        String status = "";
        double total=0;
        
       // String stid=stid_txt.getText();
      //  String sub=sub_comb.getSelectedItem().toString();
        double q1 = 0;
        double q2=0;
        double q3=0;
        double q4=0;
        double ass1=0;
        double ass2=0;
        double ass3=0;
        double mid=0;
        String subid="";
       
        
            
        String query1="select * from subject  where sub_name=?";
        try {
                pdst=con.getConnection().prepareStatement(query1);
                pdst.setString(1, sub);
            
            ResultSet rs=pdst.executeQuery();
            while(rs.next()){
                subid=rs.getString("sub_id");
            
            }
            String query2="select * from ict_ca where sub_id=? and stu_id=?";
        
            pdst=con.getConnection().prepareStatement(query2);
            //caal to getsubid function
             pdst.setString(1, subid); 
             pdst.setString(2, stid);
            
            rs=pdst.executeQuery();
            
            //get result for result set
            while(rs.next()){
                q1=rs.getDouble("q1");
                q2=rs.getDouble("q2");
                q3=rs.getDouble("q3");
                q4=rs.getDouble("q4");
                ass1=rs.getDouble("ass1");
                ass2=rs.getDouble("ass2");
                ass3=rs.getDouble("ass3");
                mid=rs.getDouble("mid");
                        
            //assign quiz marks to array
            double[] quiz={q1,q2,q3,q4};
            double[] assignment={ass1,ass2,ass3};
            
            //get min value from quiz marks
            double min=quiz[0];
            double qtot=0;
            for(int i=0;i<4;i++){
                double no=quiz[i];
                if(min>no){
                    min=no;
                }
            }
            
            //get quiz total
            for(int j=0;j<4;j++){
                 if(min!=quiz[j]){
                     qtot= qtot  + quiz[j];
                }
            }
            
            //get assignment total
            double asstot=0;
             for(int c=0;c<3;c++){
                 
                     asstot= asstot  + assignment[c];
                
            }
            
            //get quiz marks
            double quiz_marks=qtot/3;
            double ass_marks=asstot/3;
            
            //get full ca marks total
            total=(quiz_marks+ass_marks+mid)/3;
            
            if(total>=50){
                status="PASS";
            }
            else{
                 status = "FAIL";
            }
            
            //new DecimalFormat("0.00").format(quiz_marks);
            
            
            tmodel=(DefaultTableModel)table.getModel();
                    tmodel.addRow(new Object[]{
                        stid,
                        subid,
                        quiz_marks,
                        ass_marks,
                        mid,
                        total,
                        status
                        
                    });
  
            }
        } catch (SQLException ex) {
            Logger.getLogger(lec_ca.class.getName()).log(Level.SEVERE, null, ex);
        }
        
             return total;  
    }
