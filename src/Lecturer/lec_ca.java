    public String getdsubid(){
        
            
            String subid="";
            String query1="select * from subject  where sub_name=?";
            try {
                pdst=con.getConnection().prepareStatement(query1);
                pdst.setString(1, sub);
            
            ResultSet rs=pdst.executeQuery();
            if(rs.next()==false){
                JOptionPane.showMessageDialog(this, "Not Valid Subject Number");
            }else{
                while(rs.next()){
                    subid=rs.getString("sub_id");
                }
            }
            } catch (SQLException ex) {
                Logger.getLogger(lec_ca.class.getName()).log(Level.SEVERE, null, ex);
            }
            
       return subid;
    }
